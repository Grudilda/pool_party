#include "rush_01.h"

void	display_map(int **map)
{
	int	i;
	int	j;

	i = 1;
	while (i < 5)
	{
		j = 1;
		while (j < 5)
		{
			ft_putnbr(map[i][j++]);
			if (j < 5)
				ft_putchar(' ');
		}
		ft_putchar('\n');
		i++;
	}
}
